# 1. Поработайте с переменными, создайте несколько,
# выведите на экран, запросите у пользователя несколько чисел и строк и сохраните в переменные, выведите на экран.
num1 = int(input('Введите первое число: '))
num2 = int(input('Введите второе число: '))
op = input('Введите знак операции (+, -, *, /): ')
if op == '+':
    res = num1 + num2
elif op == '-':
    res = num1 - num2
elif op == '*':
    res = num1 * num2
else:
    res = num1 / num2
print(f'{num1} {op} {num2} = {res:.2f}')
print()
# 2. Пользователь вводит время в секундах.
# Переведите время в часы, минуты и секунды и выведите в формате чч:мм:сс. Используйте форматирование строк.

user_seconds = int(input('Введите количество секунд: '))

hours = user_seconds // 3600
minutes = (user_seconds // 60) - (hours * 60)
seconds = user_seconds - (minutes * 60) - (hours * 3600)

print(f"{hours:02}:{minutes:02}:{seconds:02}")
print()
